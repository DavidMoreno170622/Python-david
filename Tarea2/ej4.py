color = input("Dime tu color favorito: ").lower()

if color == "rojo":
    print("PASIÓN")
elif color == "amarillo":
    print("FELICIDAD")
elif color == "verde":
    print("ESPERANZA")
elif color == "azul":
    print("CALMA")
elif color == "morado":
    print("CREATIVIDAD")
else:
    print("Error: ese color no está en la lista.")

